    /// A slow tool handler must NOT block a fast call on the same
    /// connection. Regression test for the original "all RUNNING for
    /// 1m29s" bug: when dispatch was awaited inline in the read
    /// loop, every tool call serialized behind the previous one even
    /// though Claude sent them in parallel.
    #[tokio::test]
    async fn slow_call_does_not_block_fast_call_on_same_connection() {
        use std::time::Duration;

        struct SlowTool;
        #[async_trait]
        impl ToolHandler for SlowTool {
            fn name(&self) -> &str { "slow" }
            fn description(&self) -> &str { "sleeps for 500ms" }
            fn input_schema(&self) -> Value {
                json!({ "type": "object", "additionalProperties": true })
            }
            async fn call(&self, _args: Value) -> std::result::Result<Value, ToolHandlerError> {
                tokio::time::sleep(Duration::from_millis(500)).await;
                Ok(json!([{ "type": "text", "text": "slow done" }]))
            }
        }

        let sock = temp_sock();
        let token = uuid::Uuid::new_v4().to_string();
        let server = Server::new(sock.clone(), token.clone())
            .register_tool(Arc::new(SlowTool))
            .register_tool(Arc::new(EchoTool));
        let handle = server.serve().await.unwrap();

        let mut stream = tokio::net::UnixStream::connect(&sock).await.unwrap();
        write_frame(
            &mut stream,
            &Frame::new(json!({
                "jsonrpc": "2.0", "id": 0, "method": "operon/hello",
                "params": { "token": token, "client": "test" },
            })),
        )
        .await
        .unwrap();
        stream.flush().await.unwrap();
        let (r, mut w) = stream.into_split();
        let mut buf = BufReader::new(r);
        let _ = read_frame(&mut buf).await.unwrap();

        // Fire the slow call first, then the echo immediately after.
        // With inline-await dispatch, the echo reply would wait the
        // full 500ms. With per-call task spawning it should come
        // back almost instantly.
        write_frame(
            &mut w,
            &Frame::new(json!({
                "jsonrpc": "2.0", "id": 1,
                "method": "tools/call",
                "params": { "name": "slow", "arguments": {} },
            })),
        )
        .await
        .unwrap();
        write_frame(
            &mut w,
            &Frame::new(json!({
                "jsonrpc": "2.0", "id": 2,
                "method": "tools/call",
                "params": { "name": "operon_echo", "arguments": { "fast": true } },
            })),
        )
        .await
        .unwrap();
        w.flush().await.unwrap();

        let start = std::time::Instant::now();
        let first = read_frame(&mut buf).await.unwrap();
        let first_elapsed = start.elapsed();
        let second = read_frame(&mut buf).await.unwrap();

        // The echo should return first (it completes in <1ms; the
        // slow tool sleeps 500ms), and well under the slow tool's
        // wall-time. 200ms is plenty of slack for CI noise.
        assert_eq!(
            first.raw["id"], 2,
            "echo (id=2) should finish first; got id={:?}",
            first.raw["id"]
        );
        assert_eq!(second.raw["id"], 1, "slow (id=1) should arrive second");
        assert!(
            first_elapsed < Duration::from_millis(200),
            "echo reply took {first_elapsed:?} — slow call is blocking the read loop"
        );

        handle.shutdown().await;
    }
