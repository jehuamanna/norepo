//! Unix-socket-listening bridge server.
//!
//! `Server::serve` binds the socket, then accepts connections in a
//! loop. Each connection runs in its own task and is gated on the
//! `operon/hello` handshake. After hello succeeds the connection
//! enters an MCP-frame dispatch loop — see [`dispatch`].
//!
//! Lifetime
//! --------
//! The server runs until [`ServerHandle::shutdown`] is called or the
//! task is dropped. On shutdown the socket file is unlinked so a
//! restart of the GUI can reclaim the same path.
//!
//! Concurrency
//! -----------
//! Each accepted connection runs independently. Within one connection
//! the bridge dispatches every incoming `tools/call` as its own task
//! and funnels replies back through a single writer task — so a slow
//! handler doesn't block the read loop or block sibling calls on the
//! same connection. Tool handlers therefore see overlapping calls
//! both across and within connections; they are responsible for their
//! own internal locking.

#![cfg(unix)]

